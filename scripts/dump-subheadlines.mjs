// Dump every spot's chapter-card fields into a YAML file Leon edits by
// hand. Mirrors the field shape of content.yaml so edits paste back
// cleanly. Each spot block lists what the Reader card actually shows:
// kicker, title, subheadline (falls back to deck), region, access,
// effort, best light, maps_url. Each spot also carries a
// `subheadline_status` of "needs" or "set" so it's obvious which spots
// still need real subheadline copy written.

import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import yaml from "js-yaml";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const CONTENT = path.join(ROOT, "content.yaml");
const OUT = path.join(ROOT, "subheadlines.yaml");

const data = yaml.load(fs.readFileSync(CONTENT, "utf8"));
const chapters = data.chapters || [];
const allSpots = data.spots || [];

function spotEntry(s) {
  const hasSub = typeof s.subheadline === "string" && s.subheadline.trim().length > 0;
  return {
    id: s.id,
    title: s.title || s.id,
    kicker: s.kicker || null,
    subheadline: hasSub ? s.subheadline : (s.deck || null),
    subheadline_status: hasSub ? "set" : "needs",
    region: s.region || null,
    access: s.access || null,
    effort: s.effort || null,
    best_light: s.best_light || null,
    maps_url: s.maps_url || null,
  };
}

const byChapter = {};
const unassigned = [];
for (const ch of chapters) byChapter[ch.id] = [];
for (const s of allSpots) {
  if (s.chapter && byChapter[s.chapter]) byChapter[s.chapter].push(spotEntry(s));
  else unassigned.push(spotEntry(s));
}

const out = {};
for (const ch of chapters) {
  if (byChapter[ch.id].length) out[ch.id] = byChapter[ch.id];
}
if (unassigned.length) out._unassigned = unassigned;

const header = [
  "# Chapter card copy",
  "# ----------------",
  "# Generated by scripts/dump-subheadlines.mjs from content.yaml.",
  "# Each spot block lists every field shown on the chapter Reader card.",
  "# Edit the `subheadline` value inline; spots with `subheadline_status:",
  '# "needs"` are still falling back to the spot\'s `deck` field. Ping Claude',
  "# to wire your edits back into content.yaml.",
  "",
  "",
].join("\n");

const body = yaml.dump(out, {
  lineWidth: -1,
  sortKeys: false,
  noRefs: true,
});

fs.writeFileSync(OUT, header + body, "utf8");

const totalSet = allSpots.filter(s => typeof s.subheadline === "string" && s.subheadline.trim()).length;
const totalNeeds = allSpots.length - totalSet;
console.log(`Wrote ${allSpots.length} spots to ${path.relative(ROOT, OUT)} (${totalSet} set, ${totalNeeds} need subheadlines).`);
