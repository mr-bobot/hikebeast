#!/usr/bin/env node
//
// Scans every photoId folder under full/img/derivatives/ and emits a
// {photoId: [w, h]} sidecar so Browse can size each masonry tile from
// the first paint instead of starting at [4,3] and reshuffling once
// the images decode + applySpans runs again on `load`.
//
// HB_THUMB_DIMS (full/img/thumbs/dimensions.js) already exists and
// covers spots whose `s.image` is a flat filename matching a thumb;
// after the spots-data.js regeneration started synthesising
// `<anchor>_<basename>` filenames there were ~32 sidecar rows with no
// thumb-dim match, and those tiles all started at [4,3] before
// promoting to span-wide / span-tall once the WebP decoded. Keying
// dims by photoId (the unique derivative slug used by every photo)
// covers every spot uniformly.
//
//   PATH="/opt/homebrew/opt/node@20/bin:$PATH" node scripts/build-photo-dims.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DERIV_DIR = path.resolve(__dirname, "..", "full", "img", "derivatives");
const OUT = path.join(DERIV_DIR, "dimensions.js");

if (!fs.existsSync(DERIV_DIR)) {
  console.warn(`[photo-dims] ${DERIV_DIR} missing — skipping`);
  process.exit(0);
}

const dirs = fs.readdirSync(DERIV_DIR, { withFileTypes: true })
  .filter((e) => e.isDirectory())
  .map((e) => e.name)
  .sort();

const dims = {};
let failed = 0;
for (const photoId of dirs) {
  // w400.webp is the smallest size that's still present for every
  // derivative — cheaper to read than w1800.webp, same aspect ratio.
  const probe = path.join(DERIV_DIR, photoId, "w400.webp");
  if (!fs.existsSync(probe)) { failed++; continue; }
  try {
    const m = await sharp(probe).metadata();
    if (m.width && m.height) dims[photoId] = [m.width, m.height];
    else failed++;
  } catch {
    failed++;
  }
}

const body = `// Auto-generated by scripts/build-photo-dims.mjs.
// Maps derivative photoId -> [width, height] in pixels (from w400.webp;
// aspect ratio is preserved across the whole derivative ladder).
// Loaded by /full/browse/ + /full/saved/ + similar masonry layouts so
// every <img> tile knows its aspect ratio before the WebP decodes —
// applySpans then computes the correct row-span on first paint and
// the grid doesn't reshuffle on image load.
window.HB_PHOTO_DIMS = ${JSON.stringify(dims)};
`;
fs.writeFileSync(OUT, body);
console.log(`[photo-dims] wrote ${path.relative(process.cwd(), OUT)} (${Object.keys(dims).length} entries, ${failed} failed)`);
