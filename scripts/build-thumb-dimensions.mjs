// Reads pixel dimensions of every JPG in /full/img/thumbs/ and emits a small
// sidecar dictionary (`/full/img/thumbs/dimensions.js`) that the Browse / Saved
// pages load before rendering tiles. The dims become <img width=H height=H>
// attributes, so the browser reserves the correct box height *before* the image
// bytes arrive -- CSS-column masonry then has stable column heights from the
// first paint and the tiles stop reshuffling as images load in.
//
// Re-run after adding or replacing thumbs:  node scripts/build-thumb-dimensions.mjs

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const THUMBS_DIR = resolve(__dirname, '..', 'full', 'img', 'thumbs');
const OUT = join(THUMBS_DIR, 'dimensions.js');

// JPEG dimensions are stored in any SOFn marker (0xFFC0..0xFFCF, except 0xC4
// 0xC8 0xCC which are not SOFn). The frame data is: 2-byte length, 1-byte
// precision, 2-byte height, 2-byte width.
function readJpegSize(path) {
  const buf = readFileSync(path);
  if (buf[0] !== 0xff || buf[1] !== 0xd8) return null;  // not a JPEG (no SOI)
  let i = 2;
  while (i < buf.length - 8) {
    if (buf[i] !== 0xff) return null;  // malformed
    const marker = buf[i + 1];
    // Skip standalone markers that have no length field.
    if (marker === 0x01 || (marker >= 0xd0 && marker <= 0xd7)) { i += 2; continue; }
    const segLen = buf.readUInt16BE(i + 2);
    const isSof = marker >= 0xc0 && marker <= 0xcf && marker !== 0xc4 && marker !== 0xc8 && marker !== 0xcc;
    if (isSof) {
      const h = buf.readUInt16BE(i + 5);
      const w = buf.readUInt16BE(i + 7);
      return [w, h];
    }
    i += 2 + segLen;
  }
  return null;
}

const files = readdirSync(THUMBS_DIR).filter(f => /\.jpe?g$/i.test(f)).sort();
const dims = {};
let failed = 0;

for (const f of files) {
  const size = readJpegSize(join(THUMBS_DIR, f));
  if (size) dims[f] = size;
  else { failed += 1; console.warn('  skip:', f, '(could not read JPEG header)'); }
}

const json = JSON.stringify(dims).replace(/,"/g, ',\n  "').replace(/^\{/, '{\n  ').replace(/\}$/, '\n}');
const body = `// Auto-generated by scripts/build-thumb-dimensions.mjs.
// Maps thumb filename -> [width, height] in pixels. Loaded by /full/browse/
// and /full/saved/ so each <img> can ship intrinsic dimensions and the
// CSS-column masonry stops reshuffling as images load.
window.HB_THUMB_DIMS = ${json};
`;
writeFileSync(OUT, body);

console.log(`Wrote ${OUT}`);
console.log(`  ${Object.keys(dims).length} entries · ${failed} failed`);
